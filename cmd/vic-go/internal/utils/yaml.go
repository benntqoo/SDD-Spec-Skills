// LoadPhaseStatus loads the phase status file
func LoadPhaseStatus(cfg *config.Config) (*types.PhaseFile, error) {
    phaseFile := filepath.Join(cfg.GetVICDir(), "status", "phase.yaml")

    if !FileExists(phaseFile) {
        // Return default if doesn't exist
        return &types.PhaseFile{
            CycleID:      "default",
            CurrentPhase: 0,
            CurrentGate:  0,
            StartedAt:    time.Now().Format("2006-01-02"),
            LastUpdated:    time.Now().Format("2006-01-02"),
        }, nil
    }

    data, err := os.ReadFile(phaseFile)
    if err != nil {
        return nil, fmt.Errorf("failed to read phase file: %w", err)
    }

    var phase types.PhaseFile
    if err := yaml.Unmarshal(data, &phase); err != nil {
        return nil, fmt.Errorf("failed to parse phase file: %w", err)
    }

    return &phase, nil
}

// SavePhaseStatus saves the phase status file
func SavePhaseStatus(cfg *config.Config, phase *types.PhaseFile) error {
    phaseFile := filepath.Join(cfg.GetVICDir(), "status", "phase.yaml")

    // Ensure directory exists
    statusDir := filepath.Join(cfg.GetVICDir(), "status")
    if !FileExists(statusDir) {
        if err := os.MkdirAll(statusDir, 0755); err != nil {
            return fmt.Errorf("failed to create status directory: %w", err)
        }
    }

    data, err := yaml.Marshal(phase)
    if err != nil {
        return fmt.Errorf("failed to marshal phase: %w", err)
    }

    header := []byte(`# Phase Status - SDD Flow
# Auto-generated by vic-go
# Do not edit manually - use vic phase commands

`)
    if err := os.WriteFile(phaseFile, append(header, data...), 0644); err != nil {
        return fmt.Errorf("failed to write phase file: %w", err)
    }

    return nil
}

